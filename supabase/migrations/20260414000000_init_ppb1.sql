create or replace function public.ppb1_get_available_filters()
returns table (bimestres int[], tipos_prova text[])
language sql stable security definer set search_path = public
as $$
  select
    array_agg(distinct bimestre order by bimestre) filter (where bimestre is not null),
    array_agg(distinct tipo_prova order by tipo_prova) filter (where tipo_prova is not null)
  from "2026_ppb1"."resultados_turmas";
$$;

create or replace function public.ppb1_get_summary_cards(
  p_bimestre int,
  p_tipo_prova text
)
returns table (
  total_alunos_dia1 bigint,
  total_alunos_dia2 bigint,
  total_lidos_dia1 bigint,
  total_lidos_dia2 bigint,
  perc_dia1 numeric,
  perc_dia2 numeric
)
language sql stable security definer set search_path = public
as $$
  with base as (
    select dia_prova,
           coalesce(total_alunos_turma,0) as alunos,
           coalesce(gabaritos_lidos_cmspp,0) as lidos
    from "2026_ppb1"."resultados_turmas"
    where bimestre = p_bimestre and tipo_prova = p_tipo_prova
  )
  select
    sum(alunos) filter (where dia_prova=1)::bigint,
    sum(alunos) filter (where dia_prova=2)::bigint,
    sum(lidos)  filter (where dia_prova=1)::bigint,
    sum(lidos)  filter (where dia_prova=2)::bigint,
    case when sum(alunos) filter (where dia_prova=1) > 0
         then round(100.0 * sum(lidos) filter (where dia_prova=1)
                    / sum(alunos) filter (where dia_prova=1), 2)
         else 0 end,
    case when sum(alunos) filter (where dia_prova=2) > 0
         then round(100.0 * sum(lidos) filter (where dia_prova=2)
                    / sum(alunos) filter (where dia_prova=2), 2)
         else 0 end
  from base;
$$;

create or replace function public.ppb1_get_seduc_table(
  p_bimestre int,
  p_tipo_prova text
)
returns table (
  ure text,
  total_escolas bigint,
  total_turmas bigint,
  total_alunos bigint,
  perc_dia1 numeric,
  perc_dia2 numeric
)
language sql stable security definer set search_path = public
as $$
  select
    t.ure,
    count(distinct t.escola_id)::bigint,
    count(distinct t.turma_id)::bigint,
    sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=1)::bigint,
    case when sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=1) > 0
         then round(100.0 * sum(coalesce(t.gabaritos_lidos_cmspp,0)) filter (where t.dia_prova=1)
                    / sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=1), 2)
         else 0 end,
    case when sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=2) > 0
         then round(100.0 * sum(coalesce(t.gabaritos_lidos_cmspp,0)) filter (where t.dia_prova=2)
                    / sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=2), 2)
         else 0 end
  from "2026_ppb1"."resultados_turmas" t
  where t.bimestre = p_bimestre and t.tipo_prova = p_tipo_prova and t.ure is not null
  group by t.ure
  order by t.ure;
$$;

create or replace function public.ppb1_get_ure_table(
  p_bimestre int,
  p_tipo_prova text,
  p_ure text
)
returns table (
  escola_id text,
  escola text,
  total_turmas bigint,
  total_alunos bigint,
  perc_dia1 numeric,
  perc_dia2 numeric
)
language sql stable security definer set search_path = public
as $$
  select
    t.escola_id,
    max(t.escola) as escola,
    count(distinct t.turma_id)::bigint,
    sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=1)::bigint,
    case when sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=1) > 0
         then round(100.0 * sum(coalesce(t.gabaritos_lidos_cmspp,0)) filter (where t.dia_prova=1)
                    / sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=1), 2)
         else 0 end,
    case when sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=2) > 0
         then round(100.0 * sum(coalesce(t.gabaritos_lidos_cmspp,0)) filter (where t.dia_prova=2)
                    / sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=2), 2)
         else 0 end
  from "2026_ppb1"."resultados_turmas" t
  where t.bimestre = p_bimestre
    and t.tipo_prova = p_tipo_prova
    and t.ure = p_ure
    and t.escola is not null
  group by t.escola_id
  order by max(t.escola);
$$;

create or replace function public.ppb1_get_escola_table(
  p_bimestre int,
  p_tipo_prova text,
  p_escola_id text
)
returns table (
  turma_id text,
  turma text,
  total_alunos bigint,
  perc_dia1 numeric,
  perc_dia2 numeric
)
language sql stable security definer set search_path = public
as $$
  select
    t.turma_id,
    max(t.turma) as turma,
    sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=1)::bigint,
    case when sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=1) > 0
         then round(100.0 * sum(coalesce(t.gabaritos_lidos_cmspp,0)) filter (where t.dia_prova=1)
                    / sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=1), 2)
         else 0 end,
    case when sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=2) > 0
         then round(100.0 * sum(coalesce(t.gabaritos_lidos_cmspp,0)) filter (where t.dia_prova=2)
                    / sum(coalesce(t.total_alunos_turma,0)) filter (where t.dia_prova=2), 2)
         else 0 end
  from "2026_ppb1"."resultados_turmas" t
  where t.bimestre = p_bimestre
    and t.tipo_prova = p_tipo_prova
    and t.escola_id = p_escola_id
    and t.turma is not null
  group by t.turma_id
  order by max(t.turma);
$$;

