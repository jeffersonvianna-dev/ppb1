-- Schema Prova Paulista Bimestre 1 / 2026
create schema if not exists "2026_ppb1";

create table if not exists "2026_ppb1"."resultados_turmas" (
  id bigint generated by default as identity primary key,
  ure text,
  municipio text,
  escola text,
  turma text,
  ano_prova integer,
  bimestre integer,
  tipo_prova text,
  dia_prova integer,
  nome_prova_roxo text,
  nome_prova_laranja text,
  nome_prova_verde text,
  nome_prova_amarela text,
  id_publicacao_roxo bigint,
  id_publicacao_laranja bigint,
  id_publicacao_verde bigint,
  id_publicacao_amarela bigint,
  id_modelo_roxo bigint,
  id_modelo_laranja bigint,
  id_modelo_verde bigint,
  id_modelo_amarela bigint,
  qtd_envios_iptv_roxo integer,
  qtd_envios_iptv_laranja integer,
  qtd_envios_iptv_verde integer,
  qtd_envios_iptv_amarela integer,
  qtd_gabaritos_roxo integer,
  qtd_gabaritos_laranja integer,
  qtd_gabaritos_verde integer,
  qtd_gabaritos_amarela integer,
  data_vigencia_publicacao date,
  data_inicio_agendamento date,
  data_fim_agendamento date,
  total_alunos_turma integer,
  gabaritos_lidos_cmspp integer,
  total_gabaritos_enviados_iptv integer,
  perc_gabaritos_lidos integer,
  atualizacao timestamptz,
  escola_id text generated always as (
    md5(coalesce(ure,'') || '|' || coalesce(escola,''))
  ) stored,
  turma_id text generated always as (
    md5(coalesce(ure,'') || '|' || coalesce(escola,'') || '|' || coalesce(turma,''))
  ) stored
);

create index if not exists idx_ppb1_ure on "2026_ppb1"."resultados_turmas" (ure);
create index if not exists idx_ppb1_escola_id on "2026_ppb1"."resultados_turmas" (escola_id);
create index if not exists idx_ppb1_turma_id on "2026_ppb1"."resultados_turmas" (turma_id);
create index if not exists idx_ppb1_bim_tipo_dia on "2026_ppb1"."resultados_turmas" (bimestre, tipo_prova, dia_prova);

